function visualizeDigit(pixels, size = 28) {
  let output = '';
  for (let y = 0; y < size; y++) {
    for (let x = 0; x < size; x++) {
      output += pixels[y * size + x] > 0.5 ? '‚ñà' : ' ';
    }
    output += '\n';
  }
  return output;
}

